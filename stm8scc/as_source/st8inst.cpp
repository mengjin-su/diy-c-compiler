int operCnt(const ST8_inst_t *p)
{
	int n = 0;
	unsigned mask = 0xff0000;
	while ( mask )
	{
		if ( (mask & p->operands) )
			n++;
		mask >>= 8;
	}
	return n;
}

int pickOpr(const ST8_inst_t *p, int i)
{
	int cnt = operCnt(p);

	if ( cnt <= i )
		return 0;

	return (p->operands >> ((cnt-i-1)*8)) & 0xff;
}

bool operandIsReg(const ST8_inst_t *p, int i)
{
	int operand = pickOpr(p, i);
	return (operand == ST8_REGA  ||
			operand == ST8_REGX  ||
			operand == ST8_REGY  ||
			operand == ST8_REGSP ||
			operand == ST8_REGXH ||
			operand == ST8_REGXL ||
			operand == ST8_REGYH ||
			operand == ST8_REGYL ||
			operand == ST8_REGCC );
}
